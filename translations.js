let currentLanguage = 'ar';

function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Update HTML attributes
    const html = document.documentElement;
    html.setAttribute('lang', lang);
    html.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    
    // Update all translated elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const keys = element.getAttribute('data-i18n').split('.');
        let translation = translations[lang];
        
        keys.forEach(key => {
            translation = translation[key];
        });
        
        if (translation) {
            element.textContent = translation;
        }
    });
    
    // Update placeholders
    updatePlaceholders();
    
    // Reload category buttons and products to update language
    if (typeof loadCategoryButtons === 'function') {
        loadCategoryButtons();
    }
    if (typeof loadProducts === 'function') {
        loadProducts(typeof currentFilter !== 'undefined' ? currentFilter : 'all', 
                     typeof currentSearchTerm !== 'undefined' ? currentSearchTerm : '');
    }
}

function updatePlaceholders() {
    const lang = currentLanguage;
    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const keys = element.getAttribute('data-i18n-placeholder').split('.');
        let translation = translations[lang];
        
        keys.forEach(key => {
            translation = translation[key];
        });
        
        if (translation) {
            element.placeholder = translation;
        }
    });
}

function getTranslation(key) {
    const keys = key.split('.');
    let translation = translations[currentLanguage];
    
    keys.forEach(k => {
        translation = translation[k];
    });
    
    return translation || key;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'ar';
    document.getElementById('languageSelect').value = savedLang;
    changeLanguage(savedLang);
});
